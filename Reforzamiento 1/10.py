lista = {"Nombre" : "Gustavo", "Apellido" : "García", "Edad" : 20, "año de nacimiento" : 2003}
var2 = list(lista)
print("Mi lista es la siguiente: {}".format(var2))
var3 = list(lista.values())
print("Los valores de mi lista son {}".format(var3))
var4 = list(lista.keys())
print("Las keys de mi lista son: {}".format(var4))
var5 = list(lista.items())
print("Los items de mi lista son: {}".format(var5))